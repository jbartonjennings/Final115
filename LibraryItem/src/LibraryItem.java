
	//represent Library Items in general
	public abstract class LibraryItem {
		
	    public void itemNumber() {
	        System.out.println("4712");
	    }
	    
	    public void itemTitle() {
	        System.out.println("American Psycho.");
	        
	    }
	    
	    public void checkIn() {
	        System.out.println("03.14.19");
	    }
	    
	    public void checkOut() {
	        System.out.println("03.01.19");
	    }
	}

